    def forward(self, x):
        ori_shape = x.shape[2:]

        enc_outs = []

        x ,hw_shape= self.patch_embed(x)

        for i,enc in enumerate(self.encoder):

            x, hw_shape, out, _ = enc(x,hw_shape)
            if i!= len(self.encoder)-1:
                x,h,w = self.patch_merging[i](x,hw_shape[0],hw_shape[1])
                hw_shape = (h,w)
            enc_outs.append(out)

        for i in reversed(range(len(self.encoder))):

            x ,hw_shape, out, _= self.encoder[i](x,hw_shape)
            if i!= len(self.encoder)-1:
                x,h,w = self.skip_connections[i](x,enc_outs[i],hw_shape[0],hw_shape[1])
            x, h, w = self.patch_expanding[i](x, hw_shape[0], hw_shape[1])

            hw_shape = (h,w)

            
        seg_logits = rearrange(x, 'b (h w) d -> b h w d ', w=hw_shape[0], h=hw_shape[1])
        # seg_logits = resize(x,size=ori_shape,mode='bilinear')
        # output = self.cls_seg(seg_logits)

        return seg_logits.permute(0,3,1,2)

